
import math
import torch
import torch.nn as nn

from frameworks.pytorch.models.expressions.vgg import VGG_features, VGG


cfg = {
    'VGG11': [64, 'M', 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'VGG13': [64, 64, 'M', 128, 128, 'M', 256, 256, 'M', 512, 512, 'M', 512, 512, 'M'],
    'VGG16': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 'M', 512, 512, 512, 'M', 512, 512, 512, 'M'],
    'VGG19': [64, 64, 'M', 128, 128, 'M', 256, 256, 256, 256, 'M', 512, 512, 512, 512, 'M', 512, 512, 512, 512, 'M'],
}

def _initialize_weights(model):
    for m in model.modules():
        if isinstance(m, nn.Conv2d):
            n = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            m.weight.data.normal_(0, math.sqrt(2. / n))
            if m.bias is not None:
                m.bias.data.zero_()
        elif isinstance(m, nn.BatchNorm2d):
            m.weight.data.fill_(1)
            m.bias.data.zero_()
        elif isinstance(m, nn.Linear):
            m.weight.data.normal_(0, 0.01)
            m.bias.data.zero_()

def build_vgg(vgg_name, in_channels=1, num_classes=1, hidden=64, dropout=0.4, classifier_in_size=1024, batch_norm=True):
    features_extractor = VGG_features(in_channels, cfg[vgg_name], batch_norm=batch_norm)
    model = VGG(num_classes, features_extractor, classifier_in_size=classifier_in_size, classifier_hidden=hidden, classifier_dropout=dropout)
    _initialize_weights(model)
    return model
